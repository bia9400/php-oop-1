<?php

class Movie
{
    private $title;
    private $description;
    private $author;
    private $year;
    function __construct($filmtitle, $filmdescription, $filmauthor,$anno)
    {
        $this->title = $filmtitle;
        $this->description = $filmdescription;
        $this->author = $filmauthor;
        $this->year = $anno;
    }
    public function generateTimeDistance($annoFilm)
    {
        $dataAttuale = date("Y");
        return $dataAttuale - $annoFilm;
    }
    /**
     * Get the value of title
     */
    public function getTitle()
    {
        return $this->title;
    }

    /**
     * Set the value of title
     *
     * @return  self
     */
    public function setTitle($title)
    {
        $this->title = $title;

        return $this;
    }

    /**
     * Get the value of description
     */
    public function getDescription()
    {
        return $this->description;
    }

    /**
     * Set the value of description
     *
     * @return  self
     */
    public function setDescription($description)
    {
        $this->description = $description;

        return $this;
    }

    /**
     * Get the value of author
     */
    public function getAuthor()
    {
        return $this->author;
    }

    /**
     * Set the value of author
     *
     * @return  self
     */
    public function setAuthor($author)
    {
        $this->author = $author;

        return $this;
    }

    /**
     * Get the value of year
     */ 
    public function getYear()
    {
        return $this->year;
    }

    /**
     * Set the value of year
     *
     * @return  self
     */ 
    public function setYear($year)
    {
        $this->year = $year;

        return $this;
    }
}
